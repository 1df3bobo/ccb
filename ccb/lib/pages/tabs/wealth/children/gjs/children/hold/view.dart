import 'package:ccb/utils/scale_point_widget.dart';
import 'package:flutter/material.dart';
import 'package:get/get.dart';
import 'package:flutter_screenutil/flutter_screenutil.dart';
import 'package:wb_base_widget/wb_base_widget.dart';
import 'logic.dart';
import 'state.dart';

class GuiJinShuHoldPage extends BaseStateless {
  GuiJinShuHoldPage({Key? key}) : super(key: key);

  final logic = Get.put(GuiJinShuHoldLogic());
  final state = Get.find<GuiJinShuHoldLogic>().state;


  @override
  Widget? get titleWidget => Obx(() {
    return BaseText(
      text: "贵金属",
      fontSize: 18.sp,
      color: logic.navTitleColor.value,
    );
  });

  @override
  bool get isChangeNav => true;

  @override
  Widget? get leftItem => Obx(() {
    return IconButton(
      icon: Image.asset(
        'assets/new_images/back.png',
        width: 8.w,
        height: 16.w,
        color: logic.navActionColor.value,
      ),
      onPressed: () => Get.back(),
    );
  });

  @override
  List<Widget>? get rightAction => [
    Obx(() {
      return ScalePointWidget(
        icColor: logic.navActionColor.value,
      ).withPadding(right: 10.w, left: 10.w);
    }),
    Obx(() {
      return IconButton(
        constraints: BoxConstraints(
          minWidth: 30.w,
          minHeight: 30.w,
        ),
        onPressed: () => Get.back(),
        icon: Image.asset(
          'assets/new_images/close.png',
          width: 14.w,
          height: 14.w,
          color: logic.navActionColor.value,
        ),
      );
    }),
  ];

  @override
  Function(bool change)? get onNotificationNavChange => (v) {
    logic.navActionColor.value = v ? Colors.black : Colors.black;
    logic.navTitleColor.value = v ? Colors.black : Colors.black;
  };

  @override
  Widget initBody(BuildContext context) {
    return SingleChildScrollView(
      child: Image.asset(
        'assets/new_images/cai_fu/children/gjs/gjs_hold.png',
        fit: BoxFit.fitWidth,
      ),
    );
  }
} 